<div class="medHeader" style="padding-top:30px;">2. SedStrat Virtual Field Camp:</div>
<p>This module provides two data sets and a flexible framework for teaching sedimentology 
and stratigraphy using the Sed options in StraboSpot. The module is based around an 
exposure of fluvial clastic sediments of the Cretaceous Cedar Mountain Formation near 
Green River, UT and a double road-cut exposure of the mixed clastic-carbonate Jurassic 
Carmel Formation in the Justensen’s Flats area of the San Rafael Swell, UT. The module 
is divided into three sequential activities: 1) using StraboSpot as a field notebook, 
locating spots on a map, and adding general lithologic observations for a hand-sample 
of the student's choice; 2) differentiating and describing lithofacies at either the 
Cedar Mountain or Carmel exposures; and 3) either a qualitative exploration of 
stratigraphic sections using the provided Carmel measured section and outcrop images 
or constructing a measured section using lithologic data and outcrop images with 3D 
outcrop model images.</p>
