<h2 class="wsite-content-title">Teaching with Strabo</h2>

<p>These exercises have been constructed to provide students with virtual field experiences based in StraboSpot.
We provide PDF with text entry fields that are 'ready to go' as well as Word documents, in case instructors would 
like to make changes to better fit their course. Feedback is welcome - please direct any questions or comments to 
the authors listed on the exercise.</p>

<div class="medHeader" style="padding-top:30px;">1. Baraboo Field Experience:</div>
<p>This exercise guides students through a series of stops within Proterozoic 
metamorphosed and folded sediments and the overlying Cambrian rocks exposed in 
south-central Wisconsin. Students gain practice in recognizing and describing 
deformational structures in the 'field'.  </p>
